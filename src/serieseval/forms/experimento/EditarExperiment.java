    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        ExperimentController uc = new ExperimentController();

        if (fechaTBox.isEditValid() && fechaTBox.getValue() != null) {
            experiment.setDescription(usernameTbox.getText());
            experiment.setFecha((Date)fechaTBox.getValue());
            uc.Editar(experiment);
            ExperimentForm u = new ExperimentForm();
            u.setVisible(true);
            Container c = this.getParent();
            c.removeAll();
            c.add(u);
            c.revalidate();
            c.repaint();
        } else {
            JOptionPane.showMessageDialog(null, "Fecha incorrecta.");
        }
    }//GEN-LAST:event_saveBtnActionPerformed

