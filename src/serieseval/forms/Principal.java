    public void Menu(Boolean visible) {
        String role = Seguridad.UsuarioActual().getIdRole().getRoleName();
        switch (role) {
            case "Administrador": {
                RolAdministrador(visible);
                break;
            }
            case "Experto": {
                RolExperto(visible);
                break;
            }
            case "Usuario": {
                RolUsuario(visible);
                break;
            }
        }

    }

    private void RolAdministrador(Boolean visible) {
        resultadosMenu.setEnabled(visible);
        procesarMenu.setEnabled(visible);
        configuracionMenu.setEnabled(visible);
        cerrarSesionMenu.setEnabled(visible);
    }

