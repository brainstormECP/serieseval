    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        Boolean valido = true;
        String nombre = nombreTbox.getText();
        String[] nombreSplit;
        nombreSplit = nombre.split("[.]");

        if (nombreSplit.length != 4) {
            valido = false;
        }
        if (valido && !"weka".equals(nombreSplit[0])) {
            valido = false;
        }
        if (valido && !"classifiers".equals(nombreSplit[1])) {
            valido = false;
        }
        Dmgroup grupo = (Dmgroup) rolComboBox.getSelectedItem();
        if (valido && (nombreSplit[2] == null ? grupo.getName().toLowerCase() != null : !nombreSplit[2].equals(grupo.getName().toLowerCase()))) {
            valido = false;
        }

        if (valido) {
            AlgorithmController uc = new AlgorithmController();

            algoritmo.setName(nombreTbox.getText());
            algoritmo.setIdgroup((Dmgroup) rolComboBox.getSelectedItem());

            uc.Editar(algoritmo);
            AlgorithmForm u = new AlgorithmForm();
            u.setVisible(true);
            Container c = this.getParent();
            c.removeAll();
            c.add(u);
            c.revalidate();
            c.repaint();
        } else {
            JOptionPane.showMessageDialog(null, "Nombre no v√°lido.");
        }
    }//GEN-LAST:event_saveBtnActionPerformed

