    private void editarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarBtnActionPerformed
        // TODO add your handling code here:
        Integer selected = jTable1.getSelectedRow();
        if (selected >= 0) {
            User selectedUser = userList.get(selected);
            EditarUser u = new EditarUser(selectedUser);
            u.setVisible(true);
            Container c = this.getParent();
            c.removeAll();
            c.setLayout(new BorderLayout());
            c.add(u);
            c.setSize(300, 300);
            c.revalidate();
            c.repaint();
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un Usuario para editarlo.");
        }

    }//GEN-LAST:event_editarBtnActionPerformed

    private void eliminarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarBtnActionPerformed
        // TODO add your handling code here:
        Integer selected = jTable1.getSelectedRow();
        if (selected >= 0) {
            User selectedUser = userList.get(selected);
            UserController uc = new UserController();
            uc.Desactivar(selectedUser);
            
            UsuariosForm uf = new UsuariosForm();
            Container c = this.getParent();
            c.removeAll();
            c.setLayout(new BorderLayout());
            c.add(uf);
            c.setSize(300, 300);
            c.revalidate();
            c.repaint();
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un Usuario para eliminarlo.");
        }

    }//GEN-LAST:event_eliminarBtnActionPerformed


